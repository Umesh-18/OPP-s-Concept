package oopsconcept;

public class ElderChild extends Parent {

	public static void main(String[] args) {
		ElderChild e = new ElderChild ();
		e.m1();
		e.m2();
		m3();
		e.gpm1();
		e.gpm2();
		e.sgpm1();
		e.sgpm2();
	}
}

//NOTE: elder child extended parent and then grand parent and super grand parent.
// so it is example of multilevel inheritance